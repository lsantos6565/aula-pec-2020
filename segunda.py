primeiroNumero = int(input('Digite um número: '))
segundoNumero = int(input('Digite outro número: '))
terceiroNumero = int(input('Digite mais um número: '))

media = (primeiroNumero + segundoNumero + terceiroNumero)/3

print(f'A média entre {primeiroNumero}, {segundoNumero} e {terceiroNumero} é igual a {media}.')
